function balanceEdit(hObject1, hObject2, totalValue)
% if the values of two edit object exceeds totalValue, set hOject2 to have a
% proper value
value1=str2double(get(hObject1,'String'));

if value1>totalValue
    set(hObject1,'String',num2str(totalValue));
    value1=totalValue;
end

value2=str2double(get(hObject2,'String'));

if value1+value2>totalValue
    set(hObject2,'String',num2str(totalValue-value1));
end

